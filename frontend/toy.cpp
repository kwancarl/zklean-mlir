/// prototype
///   ::= id '(' id* ')'
///   
/// def constrainCir {f} [ZKField f] (a b c : ZKExpr f) : ZKBuilder f PUnit := do 
static std::unique_ptr<PrototypeAST> ParsePrototype() {

  if (CurTok != tok_identifier)
    return LogErrorP("Expected function name in prototype");

  std::string FnName = IdentifierStr;
  getNextToken(); // eat identifier

  // NOTE: Comment out below block to avoid enforcing field parameters to prototype
  if (CurTok != '{') 
    return LogErrorP("Expected '{f}' field parameter to circuit constraints");
  getNextToken(); // eat {
  if (CurTok != tok_identifier) 
    return LogErrorP("Expected field identifier, e.g. '{f}', to circuit constraints");
  getNextToken(); // eat field identifier
  if (CurTok != '}') 
    return LogErrorP("Expected '{f}' field parameter to circuit constraints");
  getNextToken(); // eat }
  if (CurTok != '[') 
    return LogErrorP("Expected '[ZKField f]' circuit constraint delcaration");
  getNextToken(); // eat [
  if (CurTok != tok_zkexpr) 
    return LogErrorP("Expected '[ZKField f]' circuit constraint delcaration");
  getNextToken(); // eat ZKExpr
  if (CurTok != tok_identifier) 
    return LogErrorP("Expected '[ZKField f]' circuit constraint delcaration");
  getNextToken(); // eat identifier
  if (CurTok != ']') 
    return LogErrorP("Expected '[ZKField f]' circuit constraint delcaration");
  getNextToken(); // eat ]
  // Finish parsing field parameters in constraint prototype
  // If field parameters not enforced, uncomment below
  /*
  // skip until we get to function arguments
  while (CurTok != '(')
    getNextToken();
  */
  
  if (CurTok != '(')
    return LogErrorP("Expected parameters in prototype");
		  
  // create a vector for argument names
  std::vector<std::string> ArgNames;
  // add identfiers to argument names
  while (getNextToken() == tok_identifier)
    ArgNames.push_back(IdentifierStr);
  if (CurTok != ':')
    return LogErrorP("Expected ':' to indicate argument types in prototype");

  // Check if arg types are ZKExpr field
  getNextToken(); // eat :
  if (CurTok != tok_zkexpr)
    return LogErrorP("Expected argument types to be 'ZKExpr f'");
  getNextToken(); // eat ZKExpr
  if (CurTok != tok_identifier)
    return LogErrorP("Expected field parameter to 'ZKExpr', e.g. 'ZKExpr f'");
  getNextToken(); // eat field param

  if (CurTok != ')')
    return LogErrorP("Expected ')' in prototype");
  getNextToken(); // eat ')'.
  
  // NOTE: Comment out below block to avoid enforcing constraint typing
  if (CurTok != ':')
    return LogErrorP("Expected constraints to be typed (indicated with ':')");
  getNextToken(); // eat ':'.
  if (CurTok != tok_zkbuilder)
    return LogErrorP("Expected return type to include 'ZKBuilder'"); 
  getNextToken(); // eat 'ZKBuilder'.
  if (CurTok != tok_identifier)
    return LogErrorP("Expected return type to include a field"); 
  getNextToken(); // eat field identifier
  if (CurTok != tok_punit)
    return LogErrorP("Expected return type to include 'PUnit'"); 
  getNextToken(); // eat PUnit
  if (CurTok != ':')
    return LogErrorP("Expected ':=' in constraint prototype");
  getNextToken(); // eat :
  if (CurTok != '=')
    return LogErrorP("Expected ':=' in constraint prototype");
  getNextToken(); // eat =
  if (CurTok != tok_do) 
    return LogErrorP("Expected 'do' after ':='");
  // End of processing constraint type
  // NOTE: Uncomment the below if avoiding processing constraint type
  /*
  // Skip until do
  while (CurTok != tok_do)
    getNextToken();
  */

  getNextToken(); // eat do

  return std::make_unique<PrototypeAST>(FnName, std::move(ArgNames));
}

/// definition ::= 'def' prototype expression
static std::unique_ptr<FunctionAST> ParseDefinition() {
  getNextToken(); // eat def.
  fprintf(stdout, "Calling Prototype\n");
  auto Proto = ParsePrototype();
  if (!Proto)
    return nullptr;

  // After lets, parse body
  if (auto E = ParseExpression())
    return std::make_unique<FunctionAST>(std::move(Proto), std::move(E));
  return nullptr;
}

/// toplevelexpr ::= expression
static std::unique_ptr<FunctionAST> ParseTopLevelExpr() {
  if (auto E = ParseExpression()) {
    // Make an anonymous proto.
    auto Proto = std::make_unique<PrototypeAST>("__anon_expr",
                                                std::vector<std::string>());
    return std::make_unique<FunctionAST>(std::move(Proto), std::move(E));
  }
  return nullptr;
}

