// The lexer returns tokens [0-255] if it is an unknown character, otherwise one
// of these for known things.
enum Token {
  tok_eof = -1,

  // commands
  tok_def = -2,
  tok_do  = -3,
  tok_let = -4,

  // primary
  tok_identifier = -5,
  tok_number = -6,

  // control
  tok_if = -7,
  tok_then = -8,
  tok_else = -9,
  tok_in = -10,

  // operators
  tok_binary = -11,
  tok_unary = -12,

  // var definition
  tok_var = -13,

  // types
  tok_zkexpr = -14,
  tok_zkbuilder = -15,
  tok_punit = -16
};

static std::string IdentifierStr; // Filled in if tok_identifier
static double NumVal;             // Filled in if tok_number
				  //
/// gettok - Return the next token from standard input.
static int gettok() {
  static int LastChar = ' ';

  // Skip any whitespace.
  while (isspace(LastChar))
    LastChar = getchar();

  // Recognize zklean identifiers / keywords
  // NOTE: includes optional '.' after alpha chars
  if (isalpha(LastChar)) { // identifier: [a-zA-Z][a-zA-Z0-9.]*
			   
    // accumulate identifier
    IdentifierStr = LastChar;
    while (isalnum((LastChar = getchar()))
	   || (LastChar == '.') // 
	  )
      IdentifierStr += LastChar;

    // zkLean commands def do let
    if (IdentifierStr == "def")
      return tok_def;
    if (IdentifierStr == "do")
      return tok_do;
    if (IdentifierStr == "if")
      return tok_if;
    if (IdentifierStr == "then")
      return tok_then;
    if (IdentifierStr == "else")
      return tok_else;
    if (IdentifierStr == "let")
      return tok_let;
    if (IdentifierStr == "in")
      return tok_in;
    if (IdentifierStr == "binary")
      return tok_binary;
    if (IdentifierStr == "unary")
      return tok_unary;
    if (IdentifierStr == "var")
      return tok_var;
    if (IdentifierStr == "ZKExpr")
      return tok_zkexpr;
    if (IdentifierStr == "ZKBuilder")
      return tok_zkbuilder;
    if (IdentifierStr == "PUnit")
      return tok_punit;
    return tok_identifier;
  }

  // Recognize zkLean literals
  // TODO: this should only recognize nats, but leaving as doubles for now
  if (isdigit(LastChar) || LastChar == '.') { // Number: [0-9.]+
    std::string NumStr;
    do {
      NumStr += LastChar;
      LastChar = getchar();
    } while (isdigit(LastChar) || LastChar == '.');

    NumVal = strtod(NumStr.c_str(), nullptr);
    return tok_number;
  }

  // Recognize Lean comments prefixed with '--'
  if (LastChar == '-' && getchar() == '-') {
    // Comment until end of line.
    do
      LastChar = getchar();
    while (LastChar != EOF && LastChar != '\n' && LastChar != '\r');

    if (LastChar != EOF)
      return gettok();
  }

  // Check for end of file.  Don't eat the EOF.
  if (LastChar == EOF)
    return tok_eof;

  // Otherwise, just return the character as its ascii value.
  int ThisChar = LastChar;
  LastChar = getchar();
  return ThisChar;
}




